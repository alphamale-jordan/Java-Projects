    private void cboWithdrawItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboWithdrawItemStateChanged
       
        if(cboWithdraw.getSelectedIndex()!=-1){ // if the user has selected the amount to withdraw
            if(Integer.parseInt(cboWithdraw.getSelectedItem().toString())<=balance)  //if amount selected in the combo box is less than equal to the balance
                btnOK.setEnabled(true);// Enable the OK button
            else{
                btnOK.setEnabled(false);//disable the OK button and show message below
                JOptionPane.showMessageDialog(null,"Amount to withdraw must be less than the balance : R"+balance);
            }
         }
    }//GEN-LAST:event_cboWithdrawItemStateChanged


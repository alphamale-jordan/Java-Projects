    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        if(chkWithdraw.isSelected()){
            balance = balance - Integer.parseInt(cboWithdraw.getSelectedItem().toString());
            lblBal.setText("R"+String.valueOf(balance));//set the lblBal label string to be the balance
            JOptionPane.showMessageDialog(null,"Withrawal of + R"+Integer.parseInt(cboWithdraw.getSelectedItem().toString())+" is successful.");
            //diactivate the controls
            btnOK.setEnabled(false);// disable the OK button            
            optionsGroup.clearSelection();// reset so none of the checkboxes are selected
            cboWithdraw.setSelectedIndex(-1); // select no option
            cboWithdraw.setEnabled(false);//disable the combobox
        }
        if(chkDep.isSelected()){ 
           int amount = Integer.parseInt(txtDepAmount.getText());// read deposit amount from textbox 
           //check if amount is great than zero and amount is in multiples of 10 e.g. 10, 20,30,40 etc
           if(amount>0 && amount%10==0){
               balance = balance + amount;
               lblBal.setText("R"+String.valueOf(balance));//set the lblBal label string to be the balance 
                //diactivate the controls
                btnOK.setEnabled(false);// disable the OK button            
                optionsGroup.clearSelection();// reset so none of the checkboxes are selected
                cboWithdraw.setSelectedIndex(-1); // select no option
                cboWithdraw.setEnabled(false);//disable the combobox
                txtDepAmount.setEnabled(false);//disable the amount textfield
                txtDepAmount.setText("");//clear text in text field
                JOptionPane.showMessageDialog(null,"Deposit of R"+amount+" is successful.");
            }else{
               JOptionPane.showMessageDialog(null,"Enter an amount greater than ZERO and a multiple of R10.");
           }
        }
    }//GEN-LAST:event_btnOKActionPerformed

